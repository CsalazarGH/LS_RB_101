# Arithmetic Integer

puts "Enter your first number:"
number1 = gets.chomp.to_f

puts "Enter your second number:"
number2 = gets.chomp.to_f

puts "#{number1} + #{number2} = #{number1 + number2}"
puts "#{number1} - #{number2} = #{number1 - number2}"
puts "#{number1} * #{number2} = #{number1 * number2}"
puts "#{number1} / #{number2} = #{number1 / number2}"
puts "#{number1} % #{number2} = #{number1 % number2}"
puts "#{number1} ** #{number2} = #{number1 ** number2}"